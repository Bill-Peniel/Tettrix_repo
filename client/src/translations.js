// Language management utilities
export class LanguageManager {
  constructor() {
    this.currentLanguage = this.getStoredLanguage() || 'en';
    this.translations = translations;
  }

  getStoredLanguage() {
    return localStorage.getItem('tettrix-language');
  }

  setLanguage(language) {
    if (this.translations[language]) {
      this.currentLanguage = language;
      localStorage.setItem('tettrix-language', language);
      this.updatePageTexts();
      this.updateLanguageSelector();
    }
  }

  getCurrentLanguage() {
    return this.currentLanguage;
  }

  getText(path) {
    const keys = path.split('.');
    let current = this.translations[this.currentLanguage];
    
    for (const key of keys) {
      if (current && current[key] !== undefined) {
        current = current[key];
      } else {
        console.warn(`Translation not found for: ${path}`);
        return path;
      }
    }
    
    return current;
  }

  updatePageTexts() {
    // Update navigation
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      const text = this.getText(key);
      
      if (typeof text === 'string') {
        if (element.tagName === 'INPUT' && element.type !== 'submit') {
          element.placeholder = text;
        } else {
          element.textContent = text;
        }
      }
    });

    // Update arrays (like service features)
    document.querySelectorAll('[data-i18n-array]').forEach(element => {
      const key = element.getAttribute('data-i18n-array');
      const items = this.getText(key);
      
      if (Array.isArray(items)) {
        const listItems = element.querySelectorAll('li');
        items.forEach((item, index) => {
          if (listItems[index]) {
            listItems[index].innerHTML = listItems[index].innerHTML.replace(/>.+<\//, `>${item}</`);
          }
        });
      }
    });

    // Update select options
    document.querySelectorAll('select[data-i18n-options]').forEach(select => {
      const key = select.getAttribute('data-i18n-options');
      const options = this.getText(key);
      
      if (Array.isArray(options)) {
        const optionElements = select.querySelectorAll('option:not([value=""])');
        options.forEach((option, index) => {
          if (optionElements[index]) {
            optionElements[index].textContent = option;
          }
        });
      }
    });
  }

  updateLanguageSelector() {
    const selector = document.getElementById('language-selector');
    if (selector) {
      selector.value = this.currentLanguage;
    }
  }

  init() {
    this.updatePageTexts();
    this.updateLanguageSelector();
  }
}